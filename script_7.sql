  ('moscow', 'kazan');

DROP TABLE IF EXISTS cities;
CREATE TABLE cities (
  id INT UNSIGNED NOT NULL AUTO_INCREMENT UNIQUE PRIMARY KEY,
  label VARCHAR(50),
  name VARCHAR(50)
);

INSERT INTO cities (label, name) VALUES 
  ('moscow', 'Москва'),
  ('irkutsk', 'Иркутск'),
  ('novgorod', 'Новгород'),
  ('kazan', 'Казань'),
  ('omsk', 'Омск');
 
SELECT city1.name, city2.name
FROM
  cities AS city1
JOIN
  flights
JOIN
  cities AS city2
ON
  city1.label = flights.`from` && city2.label = flights.`to`
ORDER BY flights.id;
